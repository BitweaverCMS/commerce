  class table {
    var $code, $title, $description, $icon, $enabled;

// class constructor
    function table() {
      global $order, $gBitDb;

      $this->code = 'table';
		if( defined( 'MODULE_SHIPPING_TABLE_STATUS' ) ) {
		  $this->title = MODULE_SHIPPING_TABLE_TEXT_TITLE;
		  $this->description = MODULE_SHIPPING_TABLE_TEXT_DESCRIPTION;
		  $this->sort_order = MODULE_SHIPPING_TABLE_SORT_ORDER;
		  $this->icon = '';
		  $this->tax_class = MODULE_SHIPPING_TABLE_TAX_CLASS;
		  $this->tax_basis = MODULE_SHIPPING_TABLE_TAX_BASIS;
			$this->enabled = ((MODULE_SHIPPING_TABLE_STATUS == 'True') ? true : false);
		}

      if ( ($this->enabled == true) && ((int)MODULE_SHIPPING_TABLE_ZONE > 0) ) {
        $check_flag = false;
        $check = $gBitDb->Execute("select `zone_id` from " . TABLE_ZONES_TO_GEO_ZONES . " where `geo_zone_id` = '" . MODULE_SHIPPING_TABLE_ZONE . "' and `zone_country_id` = '" . $order->delivery['country']['countries_id'] . "' order by `zone_id`");
        while (!$check->EOF) {
          if ($check->fields['zone_id'] < 1) {
            $check_flag = true;
            break;
          } elseif ($check->fields['zone_id'] == $order->delivery['zone_id']) {
            $check_flag = true;
            break;
          }
          $check->MoveNext();
        }

        if ($check_flag == false) {
          $this->enabled = false;
        }
      }
    }

// class methods
    function quote( $pShipHash = array() ) {
      global $order;

// shipping adjustment
      if (MODULE_SHIPPING_TABLE_MODE == 'price') {
        $order_total = $gBitCustomer->mCart->show_total() - $gBitCustomer->mCart->free_shipping_prices() ;
      } else {
        $order_total = $shippingWeight;
      }

      $table_cost = preg_split("#[:,]#" , MODULE_SHIPPING_TABLE_COST);
      $size = sizeof($table_cost);
      for ($i=0, $n=$size; $i<$n; $i+=2) {
        if ($order_total <= $table_cost[$i]) {
          $shipping = $table_cost[$i+1];
          break;
        }
      }

      if (MODULE_SHIPPING_TABLE_MODE == 'weight') {
        $shipping = $shipping * $shippingNumBoxes;
        // show boxes if weight
        switch (SHIPPING_BOX_WEIGHT_DISPLAY) {
          case (0):
            $show_box_weight = '';
            break;
          case (1):
            $show_box_weight = $shippingNumBoxes . ' ' . TEXT_SHIPPING_BOXES;
            break;
          case (2):
            $show_box_weight = number_format($shippingWeight * $shippingNumBoxes,2) . tra( 'lbs' );
            break;
          default:
            $show_box_weight = $shippingNumBoxes . ' x ' . number_format($shippingWeight,2) . tra( 'lbs' );
            break;
        }
      }

      $this->quotes = array('id' => $this->code,
                            'module' => MODULE_SHIPPING_TABLE_TEXT_TITLE,
							'weight' => $show_box_weight,
                            'methods' => array(array('id' => $this->code,
                                                     'title' => MODULE_SHIPPING_TABLE_TEXT_WAY,
                                                     'cost' => $shipping + MODULE_SHIPPING_TABLE_HANDLING)));

      if ($this->tax_class > 0) {
        $this->quotes['tax'] = zen_get_tax_rate($this->tax_class, $order->delivery['country']['countries_id'], $order->delivery['zone_id']);
      }

		if (zen_not_null($this->icon)) {
			$this->quotes['icon'] = $this->icon;
		}

      return $this->quotes;
    }

    function check() {
      global $gBitDb;
      if (!isset($this->_check)) {
        $check_query = $gBitDb->Execute("select `configuration_value` from " . TABLE_CONFIGURATION . " where `configuration_key` = 'MODULE_SHIPPING_TABLE_STATUS'");
        $this->_check = $check_query->RecordCount();
      }
      return $this->_check;
    }

